$(document).ready(function(){
    $(".navbar .nav-link").on('click', function(event) {

        if (this.hash !== "") {

            event.preventDefault();

            var hash = this.hash;

            $('html, body').animate({
                scrollTop: $(hash).offset().top
            }, 700, function(){
                window.location.hash = hash;
            });
        } 
    });
});

// protfolio filters
$(window).on("load", function() {
    var t = $(".portfolio-container");
    t.isotope({
        filter: ".new",
        animationOptions: {
            duration: 750,
            easing: "linear",
            queue: !1
        }
    }), $(".filters a").click(function() {
        $(".filters .active").removeClass("active"), $(this).addClass("active");
        var i = $(this).attr("data-filter");
        return t.isotope({
            filter: i,
            animationOptions: {
                duration: 750,
                easing: "linear",
                queue: !1
            }
        }), !1
    });
});

// scroller 
window.onscroll = function() { updateProgressBar(); };

function updateProgressBar() {
    var winScroll = document.body.scrollTop || document.documentElement.scrollTop;
    var height = document.documentElement.scrollHeight - document.documentElement.clientHeight;
    var scrolled = (winScroll / height) * 100;
    document.getElementById("progressBar").style.width = scrolled + "%";
}

document.addEventListener("DOMContentLoaded", function() {
  const dynamicTextElement = document.getElementById("dynamic-text");
  const textArray = [" Web Designer", " Web Developer", " Front End Developer", " Apps Designer", " Apps Developer"]; // Array of texts
  const typingDelay = 100; // Adjust the speed of typing (in milliseconds)
  const erasingDelay = 50; // Adjust the speed of erasing (in milliseconds)
  const newTextDelay = 2000; // Delay between typing each text (in milliseconds)
  
  let textArrayIndex = 0;
  let charIndex = 0;

  function typeText() {
      if (charIndex < textArray[textArrayIndex].length) {
          dynamicTextElement.textContent += textArray[textArrayIndex].charAt(charIndex);
          charIndex++;
          setTimeout(typeText, typingDelay);
      } else {
          setTimeout(eraseText, newTextDelay);
      }
  }

  function eraseText() {
      if (charIndex > 0) {
          dynamicTextElement.textContent = textArray[textArrayIndex].substring(0, charIndex - 1);
          charIndex--;
          setTimeout(eraseText, erasingDelay);
      } else {
          textArrayIndex++;
          if (textArrayIndex >= textArray.length) {
              textArrayIndex = 0;
          }
          setTimeout(typeText, typingDelay);
      }
  }

  setTimeout(typeText, newTextDelay);
});

